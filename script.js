function toggleSidebar() {
  const sidebarContent = document.getElementById('sidebar-content');
  const button = document.querySelector('[data-sidebar-btn]');
  
  if (sidebarContent.classList.contains('hidden')) {
    sidebarContent.classList.remove('hidden');
    button.querySelector('span').textContent = 'Hide Contact';
    button.querySelector('ion-icon').setAttribute('name', 'close-outline');
  } else {
    sidebarContent.classList.add('hidden');
    button.querySelector('span').textContent = 'Show Contact';
    button.querySelector('ion-icon').setAttribute('name', 'menu-outline');
  }
}

document.querySelector('[data-sidebar-btn]').addEventListener('click', toggleSidebar);
